      <h2 class="tituloL"><span id=N1>Desafíos Ambientales Globales y Locales</span></h2>
      <p class="letrasN">La creciente demanda y consumo de Artefactos Eléctricos y Electrónicos (AEE) en las últimas
        décadas ha generado un problema ambiental a nivel mundial debido a la incorrecta gestión de los
        desechos que producen. Esta problemática, silenciosa pero cada vez más peligrosa, representa una
        amenaza diaria en nuestra comunidad. Dada la riqueza ecológica de El Calafate, con tesoros como
        el Parque Nacional Perito Moreno y el Humedal Calafateño Laguna Nimez y Bahía del Lago
        Argentino, resulta urgente tomar medidas de conservación ambiental.
        </p>
        <h2 class="tituloL"><span id=N2>Importancia de la Conservación Ambiental en El Calafate</span></h2>
        <p class="letrasN">Nuestra ubicación geográfica resalta la importancia de proteger la biodiversidad y las masas de agua
          dulce que nos rodean de la contaminación generada por desechos tóxicos como metales pesados y
          contaminantes orgánicos resistentes (CPOs). Estos elementos, bioacumulables y degradables con
          dificultad, plantean riesgos significativos para los ecosistemas acuáticos y la salud humana.</p>
      <h2 class="tituloL"><span Id="N3">Educación y Acción: Abordando la Problemática de los Residuos Electrónicos</span></h2>
      <p class="letrasN">Mediante un análisis y observación detallados, hemos identificado diversos AEE, desde dispositivos
        móviles hasta electrodomésticos, que contribuyen a esta problemática. Además, se ha notado una
        desinformación generalizada en la comunidad respecto al manejo adecuado de estos desechos
        electrónicos.</p>
      <h3 class="tituloL"><span Id="N4">Beneficios Duales: Avance Educativo y Reducción del Impacto Ambiental</span>
      </h3>
      <p class="letrasN">Nuestro proyecto busca dos objetivos simultáneos. En primer lugar, mejorar la formación y el
        currículo de los alumnos a través de la incorporación de tecnologías y equipamiento educativo,
        promoviendo la colaboración con organizaciones del ámbito socio-productivo. En segundo lugar,
        aspiramos a reducir el impacto ambiental negativo por habitante en El Calafate, abordando de
        ma nera efectiva la gestión de los desechos electrónicos.
        </p>
        <h3 class="tituloL"><span Id="N5">Estrategias Multidisciplinarias para una Gestión Efectiva</span></h3>
        <p class="letrasN">Las acciones planificadas se desarrollarán en múltiples ámbitos curriculares de nuestra institución,
          incluyendo la nueva tecnicatura en Automatización y Control de Procesos Industriales (A.C.P.I.).
          Esta iniciativa representa un desafío y una oportunidad para aplicar conocimientos técnicos y
          pedagógicos en un enfoque interdisciplinario. La creación de un centro de reciclaje de Residuos de
          AEE abordará estos nuevos desafíos y se integrará en espacios curriculares y prácticas
          profesionalizantes.</p>
          <h3 class="tituloL"><span Id="N6">Impulsando un Cambio Positivo en la Comunidad</span>
          </h3>
          <p class="letrasN">En resumen, nuestro proyecto busca comprometer a toda la comunidad educativa de la Escuela
            Industrial N°9 y de El Calafate en la gestión adecuada de los Residuos de AEE, siguiendo los
            principios de la Ley de Educación Ambiental Integral 27.621 y la economía circular. Aspiramos a
            fomentar una actitud proactiva y responsable hacia el consumo y la generación de desechos
            electrónicos en nuestra localidad. Juntos, podemos generar un cambio positivo y sostenible.</p>
